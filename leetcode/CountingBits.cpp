//https://leetcode.com/problems/counting-bits/
/*
338. Counting Bits

Given an integer n, return an array ans of length n + 1 such that for each i (0 <= i <= n), ans[i] is the number of 1's in the binary representation of i.


Example 1:

Input: n = 2

Output: [0,1,1]

Explanation:
- 0 --> 0
- 1 --> 1
- 2 --> 10

Example 2:

Input: n = 5

Output: [0,1,1,2,1,2]

Explanation:
- 0 --> 0
- 1 --> 1
- 2 --> 10
- 3 --> 11
- 4 --> 100
- 5 --> 101


Constraints:

0 <= n <= 105


Follow up:

It is very easy to come up with a solution with a runtime of O(n log n). Can you do it in linear time O(n) and possibly in a single pass?
Can you do it without using any built-in function (i.e., like __builtin_popcount in C++)?

Observations:

As the follow-up mentiond: it's intuitive to come out a NlogN solution by looping from 0 to N and for each given number, keeping >>1 to tell if the bit is 1 or 0.

Obviously, it asks for a linear solution. It shouldn't take much time to realize the memorization may be the best bet.

My intuitive observation is:

4 --> 100
...
7 --> 111

So 7 can be divided as 100 + 011, therefore ans[7] = ans[4] + ans[3] = 1 + ans[3], considering the fact that the index for the first operator, "ans[4]", is always N power of 2.
So the answer for any N power of 2 is always 1. This implementation is given in countBits_my.

A popular solution online without using bitwise operator, however sharing the same idea is given in countBits_popular. The memorization or state transformation, is through:
counts[i] = counts[i - base / 2] + 1

The best one and the shortest solution is using the transformation that:

ans[i] = ans[i & (i - 1)] + 1;

101 -> 100 + 1
111 -> 110 + 1
100 -> 000 + 1

i & (i - 1) drops the lowest 1 bit from i and presents you a number having N - 1 counts of bit 1, if N is the counts of bit 1 for i.

It's interesting to see how it magically covers the edge case when i is equal to the power of 2: in that case, i & (i - 1) is always 0. This is also the means to check
if a number is a power of 2.

Happy New Year!
*/
class SolutionCountingBits {
public:
	vector<int> countBits(int n) {
		vector<int> ans(n + 1, 0);
		for (int i = 1; i <= n; ++i)
			ans[i] = 1 + ans[i & (i - 1)];
		return ans;
	}
	vector<int> countBits_popular(int n) {
		vector<int> counts(n + 1, 0);
		int base = 1;
		for (int i = 1; i <= n; ++i) {
			if (i == base) {
				counts[i] = 1;
				base *= 2;
			}
			else {
				counts[i] = counts[i - base / 2] + 1;
			}
		}
		return counts;
	}
	vector<int> countBits_my(int n) {
		size_t len = n + 1;
		vector<int> ans(len, 0);
		for (int i = 1, j = 0; i < len; ++i) {
			if (((i + 1) & i) == 0)
				ans[i] = ++j;
			else
				ans[i] = 1 + ans[(1 << j) ^ i];
		}
		return ans;
	}
};
